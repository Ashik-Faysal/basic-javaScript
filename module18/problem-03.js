/* You are given three numbers 13, 79, 45. Write a program that will print the largest number using if-else. */

var number1 = 13;
var number2 = 79;
var number3 = 45;
if (number1 && number2 < number3) {
  console.log("45 is large");
} else if (number1 && number3 < number2) {
  console.log("79 is large number");
} else {
  console.log("13 is large");
}
